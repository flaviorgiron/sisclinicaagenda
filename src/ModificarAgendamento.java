    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (jComboBox1.getSelectedItem().toString().equals("SELECIONE")) {
            Mensagem.showMessageDialog("Agenda não informada.", "");
            jComboBox1.requestFocus();
            return;
        }

        if (jTextField1.getText().trim().equals("")) {
            Mensagem.showMessageDialog("Paciente não informado.", "");
            jTextField1.requestFocus();
            return;
        }

        if (jComboBox2.getSelectedItem().toString().equals("SELECIONE")) {
            Mensagem.showMessageDialog("Motivo não informado.", "");
            jComboBox2.requestFocus();
            return;
        }

        if (jDateChooser1.getDate() == null) {
            Mensagem.showMessageDialog("Data não informada.", "");
            jDateChooser1.requestFocus();
            return;
        }

        if (jFormattedTextField1.getText().trim().equals("")) {
            Mensagem.showMessageDialog("Hora não informada.", "");
            jFormattedTextField1.requestFocus();
            return;
        }

        if (jFormattedTextField1.getText().trim().equals("00:00")) {
            Mensagem.showMessageDialog("Hora não informada.", "");
            jFormattedTextField1.requestFocus();
            return;
        }

        if (jComboBox3.getSelectedItem().toString().equals("SELECIONE")) {
            Mensagem.showMessageDialog("Secretária não informada.", "");
            jComboBox3.requestFocus();
            return;
        }

        Agendamento a = new Agendamento();
        a.setCodigo(null);
        a.setData(jDateChooser1.getDate());
        a.setExame(jComboBox2.getSelectedItem().toString());
        a.setHora(jFormattedTextField1.getText());
        a.setAgenda(jComboBox1.getSelectedItem().toString());
        a.setObservacao(jTextArea1.getText());
        a.setPaciente(jTextField1.getText());
        a.setSecretaria(jComboBox3.getSelectedItem().toString());
        if (AgendamentosDB.insertOrUpdate(a)) {
            Mensagem.showMessageDialog("Agendamento inserido com sucesso.", "");
            jComboBox1.setSelectedIndex(0);
            jComboBox2.setSelectedIndex(0);
            jComboBox3.setSelectedIndex(0);
            jTextField1.setText("");
            jTextArea1.setText("");
            jDateChooser1.setDate(null);
            jFormattedTextField1.setText("00:00");
        }
///////        jButton1.doClick();
    }//GEN-LAST:event_jButton2ActionPerformed

